def weighted_quantile(values, weights, alpha):
    # Sort losses from smallest to largest
    sorted_index = np.argsort(values)
    sorted_values = values[sorted_index]
    sorted_weights = weights[sorted_index]

    # Cumulative probability using weights
    cumulative_weights = np.cumsum(sorted_weights)
    cutoff = alpha * cumulative_weights[-1]

    return sorted_values[np.searchsorted(cumulative_weights, cutoff)]


def calculate_weighted_risk_metrics(losses, weights, alphas=ALPHAS):
    results = {
        "Weighted_EL": np.sum(weights * losses) / np.sum(weights),
        "Weight_Mean": weights.mean(),
        "Weight_Std": weights.std(),
    }

    for alpha in alphas:
        var = weighted_quantile(losses, weights, alpha)

        tail = losses >= var
        es = np.sum(weights[tail] * losses[tail]) / np.sum(weights[tail])

        results[f"Weighted_VaR_{alpha:.1%}"] = var
        results[f"Weighted_ES_{alpha:.1%}"] = es

    results["Weighted_Capital_Buffer_VaR_99.9_minus_EL"] = (
        results["Weighted_VaR_99.9%"] - results["Weighted_EL"]
    )

    return results


# Importance Sampling shifts the mean of the sampling distribution, but the shape (normal) remains
# Why shift to the left when extreme losses are to the right of the PDF? X-axis on Vasicek Model is not credit losses but MACRO factors (Z)
# Vasicek model focuses on companies' correlation to the MACROECONOMY (Z), shift to the left = focus  on downturn scenarios = greater losses

def simulate_vasicek_importance_sampling(mu_shift=-2.0):
    threshold = norm.ppf(PD)

    # Shift macro factor toward crisis
    Z_shifted = np.random.normal(mu_shift, 1, size=N_SIMULATIONS)

    # Company-specific shocks stay normal
    eps = np.random.normal(0, 1, size=(N_SIMULATIONS, N_BORROWERS))

    asset = np.sqrt(RHO) * Z_shifted[:, None] + np.sqrt(1 - RHO) * eps

    defaults = (asset < threshold).astype(int)

    losses = defaults.sum(axis=1) * LGD * EAD

    # Importance sampling weight = original density / shifted density
    weights = norm.pdf(Z_shifted, loc=0, scale=1) / norm.pdf(Z_shifted, loc=mu_shift, scale=1)

    return losses, weights

# Run importance sampling
is_losses, is_weights = simulate_vasicek_importance_sampling(mu_shift=-2.0)

