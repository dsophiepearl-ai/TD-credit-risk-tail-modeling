"""
Credit Risk Monte Carlo Project
Thesis:
Gaussian Vasicek-style credit portfolio models may underestimate tail risk and capital buffers
because they underrepresent extreme macro shocks and joint default clustering. This script starts
with a baseline Vasicek Monte Carlo model, then compares it to a fat-tailed Student-t factor model.

Core outputs:
- Expected Loss (EL)
- VaR at 99% and 99.9%
- Expected Shortfall / CVaR at 99% and 99.9%
- Implied unexpected-loss capital buffer = VaR_99.9 - EL
"""

import numpy as np
import pandas as pd
from scipy.stats import norm, t
import matplotlib.pyplot as plt


# ============================================================
# 1. Global parameters
# ============================================================

np.random.seed(42)

# Portfolio assumptions
N_BORROWERS = 1000
N_SIMULATIONS = 100_000

PD = 0.02          # annual probability of default: 2%
LGD = 0.45         # loss given default: 45%
EAD = 1_000_000    # exposure at default per borrower
RHO = 0.15         # asset correlation / systematic factor loading

# Student-t assumptions
T_DF = 4           # degrees of freedom; lower df = fatter tails

# Risk levels
ALPHAS = [0.99, 0.999]

# ============================================================
# 2. Helper functions
# ============================================================

def calculate_risk_metrics(losses, alphas=ALPHAS):
    """
    Calculate EL, VaR, ES, and capital buffer from simulated portfolio losses.

    EL = average loss
    VaR_alpha = alpha quantile of loss distribution
    ES_alpha = average loss conditional on loss > VaR_alpha
    Capital buffer = VaR_99.9 - EL
    """
    losses = np.asarray(losses)
    results = {
        "EL": losses.mean(),
        "Std Loss": losses.std(),
    }

    for alpha in alphas:
        var = np.quantile(losses, alpha)
        es = losses[losses >= var].mean()
        results[f"VaR_{alpha:.1%}"] = var
        results[f"ES_{alpha:.1%}"] = es

    if 0.999 in alphas:
        results["Capital_Buffer_VaR_99.9_minus_EL"] = results["VaR_99.9%"] - results["EL"]

    return results


def simulate_vasicek_gaussian(
    n_borrowers=N_BORROWERS,
    n_simulations=N_SIMULATIONS,
    pd=PD,
    lgd=LGD,
    ead=EAD,
    rho=RHO,
):
    """
    Gaussian Vasicek one-factor model.

    Latent borrower health:
        A_i = sqrt(rho) * Z + sqrt(1-rho) * epsilon_i

    Default rule:
        D_i = 1 if A_i < default_threshold

    Z = systematic macro factor
    epsilon_i = borrower-specific shock
    rho = asset correlation / strength of common macro factor
    """

    # Threshold = z-score corresponding to the probability of default (2%)
    # Reverse engineer z-score formula to look for z instead of probability using norm.ppf
    # Below what threshold of companies' financial health can we expect a default?
    threshold = norm.ppf(pd)

    # Systematic macro shock: one per scenario
    Z = np.random.normal(0, 1, size=n_simulations)

    # Idiosyncratic shocks: one per borrower per scenario
    # Use two arrays of n to create a matrix 
    eps = np.random.normal(0, 1, size=(n_simulations, n_borrowers))

    # Latent asset/financial health
    # "None" transforms 1D list output of Z (Macro Economy) into a matrix shape so that it can be calculated with epsilon (Micro: Idyosincratic Risk)
    asset = np.sqrt(rho) * Z[:, None] + np.sqrt(1 - rho) * eps

    # Default indicator: Bernoulli outcome (Binary: 0 = no default / 1 = default) generated by threshold rule
    # Use astype(int) to convert True/False default output into a Bernoulli (binary) variable
    defaults = (asset < threshold).astype(int)

    # Portfolio loss per scenario
    # borrowers = columns / scenarios = rows, axis=1 --> SUM OF EACH SCENARIO (rows) across all borrowers (columns)
    # axis=0: opposite --> sum up each column across all rows
    losses = defaults.sum(axis=1) * lgd * ead

    return losses



def simulate_student_t_factor(
    n_borrowers=N_BORROWERS,
    n_simulations=N_SIMULATIONS,
    pd=PD,
    lgd=LGD,
    ead=EAD,
    rho=RHO,
    df=T_DF,
):
    """
    Fat-tailed one-factor model using Student-t shocks.

    Main difference vs Gaussian Vasicek:
    - Gaussian factor assumes macro shocks are thin-tailed.
    - Student-t factor gives more probability to extreme macro shocks.

    This creates stronger crisis default clustering and a fatter loss tail.
    """

    # Use t threshold so unconditional default probability is roughly pd under t shocks
    threshold = t.ppf(pd, df=df)

    # Systematic fat-tailed macro shock
    #np.random.normal() and t.rvs() do the same thing — they both generate random samples from a normal distribution
    # MONTE CARLO (Z & eps): simulate many random scenarios
    Z = t.rvs(df=df, size=n_simulations)

    # Idiosyncratic fat-tailed shocks
    eps = t.rvs(df=df, size=(n_simulations, n_borrowers))

    # Standardize t shocks to variance approximately 1 when df > 2
    scale = np.sqrt(df / (df - 2))
    Z = Z / scale
    eps = eps / scale
    threshold = threshold / scale
    
    # MONTE CARLO OUTPUTS
    asset = np.sqrt(rho) * Z[:, None] + np.sqrt(1 - rho) * eps
    defaults = (asset < threshold).astype(int)
    losses = defaults.sum(axis=1) * lgd * ead

    return losses

