# STABILITY TEST
# 2. Compare standard MC vs Importance Sampling stability

seeds = range(30)

mc_vars = []
is_vars = []

for seed in seeds:
    np.random.seed(seed)

    # Standard MC
    mc_losses = simulate_vasicek_gaussian()
    mc_var_999 = np.quantile(mc_losses, 0.999)
    mc_vars.append(mc_var_999)

    # Importance Sampling
    is_losses, is_weights = simulate_vasicek_importance_sampling(mu_shift=-2.0)
    is_var_999 = weighted_quantile(is_losses, is_weights, 0.999)
    is_vars.append(is_var_999)

stability = pd.DataFrame({
    "Standard_MC_VaR_99.9": mc_vars,
    "Importance_Sampling_VaR_99.9": is_vars
})

print("\nVaR Stability Across Seeds")
print(stability.describe())

plt.figure(figsize=(10,5))

plt.plot(mc_vars, marker='o', label='Standard MC VaR 99.9%')
